package org.jooq.impl;

import java.sql.Connection;
import java.sql.ResultSet;
import java.sql.SQLException;
import java.sql.Statement;

import org.jooq.tools.jdbc.DefaultCallableStatement;

/**
 * @author Lukas Eder
 */
final class ParsingStatement extends DefaultCallableStatement {

    private final ParsingConnection connection;

    ParsingStatement(ParsingConnection connection, Statement statement) {
        super(statement);

        this.connection = connection;
    }

    @Override
    public final ResultSet executeQuery(String sql) throws SQLException {
        return super.executeQuery(connection.translate(sql));
    }

    @Override
    public final int executeUpdate(String sql) throws SQLException {
        return super.executeUpdate(connection.translate(sql));
    }

    @Override
    public final int executeUpdate(String sql, int autoGeneratedKeys) throws SQLException {
        return super.executeUpdate(connection.translate(sql), autoGeneratedKeys);
    }

    @Override
    public final int executeUpdate(String sql, int[] columnIndexes) throws SQLException {
        return super.executeUpdate(connection.translate(sql), columnIndexes);
    }

    @Override
    public final int executeUpdate(String sql, String[] columnNames) throws SQLException {
        return super.executeUpdate(connection.translate(sql), columnNames);
    }

    @Override
    public final boolean execute(String sql) throws SQLException {
        return super.execute(connection.translate(sql));
    }

    @Override
    public final boolean execute(String sql, int autoGeneratedKeys) throws SQLException {
        return super.execute(connection.translate(sql), autoGeneratedKeys);
    }

    @Override
    public final boolean execute(String sql, int[] columnIndexes) throws SQLException {
        return super.execute(connection.translate(sql), columnIndexes);
    }

    @Override
    public final boolean execute(String sql, String[] columnNames) throws SQLException {
        return super.execute(connection.translate(sql), columnNames);
    }

    @Override
    public final void addBatch(String sql) throws SQLException {
        super.addBatch(connection.translate(sql));
    }

    @Override
    public final Connection getConnection() throws SQLException {
        return connection;
    }
}
